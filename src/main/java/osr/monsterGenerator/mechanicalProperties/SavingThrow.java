package osr.monsterGenerator.mechanicalProperties;

// These may vary wildly based on system
public class SavingThrow {
    private String name;
    private int value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
